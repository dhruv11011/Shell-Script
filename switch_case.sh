echo "Enter Number Between 1 to 5"
read a

case "$a" in
	"1") echo "You Choose Number 1"
	;;
	"2") echo "You Choose Number 2"
	;;
	"3") echo "You Choose Number 3"
	;;
	"4") echo "You Choose Number 4"
	;;
	"5") echo "You Choose Number 5"
	;;
esac #Every Loop Close With Reverse String Of it's Start like if=>fi case=>esac
	
echo "Thank You!"
	
